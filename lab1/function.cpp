#include <iostream>

int main() {
    int var1 = 10;
    int var2 = 25;
    std::cout << "var1 + var2 = " << (var1 + var2) << std::endl;
    std::cout << "var1 - var2 = " << (var1 - var2) << std::endl;
    std::cout << "var1 / var2 = " << (var1 / var2) << std::endl;
    std::cout << "var1 * var2 = " << (var1 * var2) << std::endl;
    std::cout << "var1 % var2 = " << (var1 % var2) << std::endl;
    return 0;
}
